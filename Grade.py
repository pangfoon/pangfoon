#โปรแกรมการตัดเกรด
SCORE=float(input("STUDENT SCORE:"))
if 80<=SCORE<=100:
    print("A")
elif 75<=SCORE<=80:
    print("B+")
elif 70<=SCORE<=75:
    print("B")
elif 65<=SCORE<=70:
    print("C+")
elif 60<=SCORE<=65:
    print("C")
elif 55<=SCORE<=60:
    print("D+")
elif 50<=SCORE<=55:
    print("D")
elif 0<=SCORE<=50:
    print("F")
#ถ้าใส่คะแนนเกินโปรแกรมจะทำงานส่วนของ else 
else:
    print("ERROR")
    





